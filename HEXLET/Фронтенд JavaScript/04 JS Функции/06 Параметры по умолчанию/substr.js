/*
Реализуйте и экспортируйте по умолчанию функцию substr, которая возвращает подстроку из
переданной строки. Подстрокой называется любая выбранная часть строки. Например для строки
hello можно выделить следующие подстроки: ello, llo, o, el и множество других.

Функция принимает на вход три параметра:
1. Строка.
2. Начальный индекс. Значение по умолчанию: 0.
3. Длина подстроки (количество символов). Значение по умолчанию: длина всей строки.

По умолчанию, то есть если не переданы дополнительные параметры, возвращается сама строка.

У этой функции множество вариантов поведения:
Если переданная длина отрицательная, то внутри она становится единицей
Если переданная длина равна нулю, то возвращается пустая строка
Если конечный индекс (начальный индекс + переданная длина - 1) выходит за границу строки,
то берётся подстрока от начального индекса до конца строки.
Если начальный индекс отрицательный, то внутри он становится нулем
Если начальный индекс выходит за границу слова справа, то всегда возвращается пустая строка

Для реализации этой функции, используйте подход, который называется "нормализация". Он заключается в том, что
перед тем как делать вычисления, данные приводятся к виду, с которым удобно работать.
Например, длина строки для substr может иметь следующие неудобные значения:
1. Отрицательное число
2. Число, выходящее за границу строки
3. "Индекс + Длина" выходят за границу строки

В каждой из этих ситуаций длину строки можно изменить на такое значение, которое будет удобно для обработки
сразу всех возможных случаев. Если число отрицательное, то длина становится равной единице (из условия),
если оно выходит за границу строки, то его нужно сделать равным длине строки. В последнем случае длина
должна стать числом "Длина - Индекс". После этих манипуляций процесс взятия подстроки становится
универсальным и не содержащим условия.
*/

const assert = require('assert');

const substr = (str, begin = 0, lnth = str.length) => {
    let mybegin = begin;
    let mylnth = lnth;
    let result = ''

    if (mylnth === 0) {
        return result;
    }
    if (mybegin < 0) {
        mybegin = 0;
    }
    if (mylnth < 0) {
        mylnth = 1;
    }
    if (mybegin + mylnth - 1 > str.length - 1) {
        mylnth = str.length - mybegin;
    }
    if (mybegin >= str.length) {
        return result;
    }

    for (let i = mybegin; i < mybegin + mylnth; i++) {
        result += str[i];
    }
    return result;
}

